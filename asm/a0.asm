;This program blink LED connected to any port.
;2011/03/16-12:21Hrs
;A1 original file
;Originnaly written by me
LIST P=16F877A
INCLUDE"P16F877A.INC"
ORG	0x0000
NOP
BANKSEL	ADCON1
MOVLW	0x0006
MOVWF	ADCON1
CLRF	TRISA
CLRF	TRISB
CLRF	TRISC
CLRF	TRISD
CLRF	TRISE
ON_OFF:	BANKSEL	PORTA
	MOVLW	0x001F
	MOVWF	PORTA
	MOVLW	0x00FF
	MOVWF	PORTB
	MOVLW	0x00FF
	MOVWF	PORTC
	MOVLW	0x00FF
	MOVWF	PORTD
	MOVLW	0x0007
	MOVWF	PORTE
	CALL	DELAY0
	BANKSEL	PORTA
	MOVLW	0x0000
	MOVWF	PORTA
	MOVLW	0x0000
	MOVWF	PORTB
	MOVLW	0x0000
	MOVWF	PORTC
	MOVLW	0x0000
	MOVWF	PORTD
	MOVLW	0x0000
	MOVWF	PORTE
	CALL	DELAY0
	GOTO	ON_OFF
DELAY0:	MOVLW	0x00FA
	MOVWF	TIME1
DELAY1:	NOP
	DECFSZ	TIME1,F
	GOTO	DELAY1
	MOVLW	0x00FA
	MOVWF	TIME2
DELAY2:	NOP
	DECFSZ	TIME2,F
	GOTO	DELAY2
	MOVLW	0x0004
	MOVWF	TIME3
DELAY3:	NOP
	DECFSZ	TIME3,F
	GOTO	DELAY3
	GOTO	DELAY0
	RETURN
END
